  `GTS_NOTIFICATION_ID` BIGINT NOT NULL,
  `GTS_NOTIFICATION_EVENT_ID` BIGINT NOT NULL,
  `GTS_NOTIFICATION_MESSAGE` VARCHAR(100) NOT NULL,
  `GTS_NOTIFICATION_RECEIVER_USER_ID` BIGINT DEFAULT NULL,
  `GTS_NOTIFICATION_RECEIVER_ROLE_ID` INT NOT NULL,
  `GTS_NOTIFICATION_DATE` DATE NOT NULL,
