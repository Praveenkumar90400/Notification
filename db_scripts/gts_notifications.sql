  PRIMARY KEY(GTS_NOTIFICATION_ID),
  FOREIGN KEY(GTS_NOTIFICATION_EVENT_ID) REFERENCES `GTS_NOTIFICATION_EVENTS`(GTS_NOTIFICATION_EVENT_ID)
   );

